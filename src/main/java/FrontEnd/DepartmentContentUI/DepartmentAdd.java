    private void generateDepartmentId() {
        // Tạo mã phòng ban theo mẫu "MP" + số
        departmentId = "MP" + String.format("%02d", departmentCount);
        txtMaPhong.setText(departmentId);
    }
    @Override
public void actionPerformed(ActionEvent e) {
    if (e.getSource() == btnAddPhong) {
        // Lấy thông tin từ các trường
        String maPhong = txtMaPhong.getText();
        String tenPhong = txtTenPhong.getText();
        String truongPhong = txtTruongPhong.getText();
        
        // Kiểm tra xem các trường có rỗng không
        if (maPhong.isEmpty() || tenPhong.isEmpty() || truongPhong.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin");
            return;
        }
        
        // Thêm thông tin vào bảng tbPhongBan
        DefaultTableModel model = (DefaultTableModel) tblPb.getModel();
        model.addRow(new Object[]{maPhong, tenPhong, truongPhong});
        
        // Xóa trống các trường
        txtMaPhong.setText("");
        txtTenPhong.setText("");
        txtTruongPhong.setText("");
        
        // Tăng số lượng phòng ban đã tạo lên 1
        departmentCount++;
        
        // Tạo lại mã phòng ban mới
        generateDepartmentId();
    }
} 
public Object[][] getTableData() {
        DefaultTableModel model = (DefaultTableModel) tblPb.getModel();
        int rowCount = model.getRowCount();
        int columnCount = model.getColumnCount();
        Object[][] data = new Object[rowCount][columnCount];
        for (int i = 0; i < rowCount; i++) {
            for (int j = 0; j < columnCount; j++) {
                data[i][j] = model.getValueAt(i, j);
            }
        }
        return data;
}
