    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        // Lấy chỉ số của dòng được chọn trong bảng
        int selectedRow = tblPb.getSelectedRow();

        // Kiểm tra xem có dòng nào được chọn không
        if (selectedRow != -1) {
            // Lấy mô hình của bảng
            DefaultTableModel model = (DefaultTableModel) tblPb.getModel();

            // Cập nhật dữ liệu cho dòng được chọn
            model.setValueAt(txtMaPhong.getText(), selectedRow, 0);
            model.setValueAt(txtTenPhong.getText(), selectedRow, 1);
            model.setValueAt(txtTruongPhong.getText(), selectedRow, 2);

            // Hiển thị thông báo xác nhận cho người dùng
            JOptionPane.showMessageDialog(this, "Thông tin đã được cập nhật.");
        } else {
            // Nếu không có dòng nào được chọn, hiển thị thông báo lỗi
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hàng để cập nhật.");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPb.getSelectedRow();

        // Kiểm tra xem có dòng nào được chọn không
        if (selectedRow != -1) {
            // Hiển thị hộp thoại xác nhận
            int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa dòng này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);

            // Nếu người dùng chọn "Yes" (Có), thực hiện xóa
            if (option == JOptionPane.YES_OPTION) {
                DefaultTableModel model = (DefaultTableModel) tblPb.getModel();
                model.removeRow(selectedRow);
            }
        } else {
            // Nếu không có dòng nào được chọn, hiển thị thông báo lỗi
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hàng để xóa.");
        }
    }//GEN-LAST:event_btnDelActionPerformed

